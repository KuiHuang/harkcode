# include <stdio.h>

void main()
{
	/* 定义了一个无符号字符型变量，此变量只能用来存储无符号数 */
	unsigned char result;
    
	int a, b, c, d;
	a = 2;//0010
	b = 4;//0100
	c = 6;//0110
	d = 8;//1000

	/* 对变量进行“按位与”操作 */
	result = a & c;// 2
	printf("result = %d\n", result);

	/* 对变量进行“按位或”操作 */
	result = b | d;//12
	printf("result = %d\n", result);

	/* 对变量进行“按位异或”操作 */
	result = a ^ d;//10
	printf("result = %d\n", result);

	/* 对变量进行“取反”操作 */
	result = ~a;    //255-2
	printf("result = %d\n", result);
}